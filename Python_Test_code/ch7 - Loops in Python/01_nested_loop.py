for row in range(1, 6):       # This loop goes through each row
    for col in range(1, 6):   # This loop goes through each column in a row
        print(row * col, end=" ")  # Print the product of row and col with a tab space
    print()  # Move to the next line after each row



# Understanding Rows and Columns
    
# Rows are horizontal lines in the table.
# Columns are vertical lines in the table.

